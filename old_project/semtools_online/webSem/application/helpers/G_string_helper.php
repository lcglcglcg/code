/**
 *---------------------------------
 * 判断字符串是否为空, 如果为空,将字符串替换成自己设置的值
 *---------------------------------
 *
 * @param str 		String	需要过滤的字符串
 * @param rep_str	String	将要替换成的字符串
 * @return 替换后的字符串
 */
function check_empty($str, $rep_str = ''){
	if(empty($str)){
		return $rep_str ;
	}
	return $str;
}